package se.xperjon.shapes.core;

public abstract class WidthHeightShape extends Shape{

	protected int width;
	protected int height;

	public WidthHeightShape(int width, int height) {
		super();
		this.width = width;
		this.height = height;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	
}
